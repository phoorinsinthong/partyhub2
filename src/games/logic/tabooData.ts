const FUNNY_START_INDEX = 132;

export function getRandomCards(count = 1, excludeWords = [], mode = 'all') {
  let base = TABOO_CARDS;
  if (mode === 'funny') base = TABOO_CARDS.slice(FUNNY_START_INDEX);
  else if (mode === 'normal') base = TABOO_CARDS.slice(0, FUNNY_START_INDEX);
  const available = base.filter(c => !excludeWords.includes(c.word));
  const pool = available.length >= count ? available : base;
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

